/*

(90 - 99) => A
(80 - 89) => B
(70 - 79) => C
(60 - 69) => D


*/

let student_percentage = 68;

if (student_percentage >= 90 && student_percentage <= 99) {
    console.log("GRAGE => A")
}
else if (student_percentage >= 80 && student_percentage <= 89) {
    console.log("GRAGE => B")
}
else if (student_percentage >= 70 && student_percentage <= 79) {
    console.log("GRAGE => C")
}
else if (student_percentage >= 60 && student_percentage <= 69) {
    console.log("GRAGE => D")
}
